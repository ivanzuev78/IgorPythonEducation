print('Рассчет индекса массы тела для трёх человек\n')

man1 = {
    'Рост': 182,
    'Вес': 80
}

man2 = {
    'Рост': 178,
    'Вес': 98
}

man3 = {
    'Рост': 187,
    'Вес': 67
}

man1['Индекс массы тела'] = 80 / ((182 / 100) ** 2)
man2['Индекс массы тела'] = 98 // ((178 / 100) ** 2)
man3['Индекс массы тела'] = 67 // ((187 / 100) ** 2)

print(man1, '\n')
print(man2, '\n')
print(man3, '\n')
