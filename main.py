print('Рассчет индекса массы тела для трёх человек\n')

man1 = {
    'Рост': 200,
    'Вес': 80
}

man2 = {
    'Рост': 178,
    'Вес': 98
}

man3 = {
    'Рост': 187,
    'Вес': 67
}

man1['Индекс массы тела'] = man1['Вес'] / ((man1["Рост"] / 100) ** 2)
man2['Индекс массы тела'] = man2['Вес'] // ((man2["Рост"] / 100) ** 2)
man3['Индекс массы тела'] = man3['Вес'] // ((man3["Рост"] / 100) ** 2)

print(man1, '\n')
print(man2, '\n')
print(man3, '\n')

