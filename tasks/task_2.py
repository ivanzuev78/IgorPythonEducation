"""
Посчитать попарную сумму элементов двух списков.
Гарантируется, что списки одинаковой длинны
Пример:
[1, 3, 5]
 +  +  +
[2, 4, 6]
 =  =  =
[3, 7, 11]

"""

list_1 = [1, 3, 5, 7, 10]
list_2 = [2, 4, 6, 7, 21]
list_3 = []

for i in range(len(list_1)):
    a = list_1[i] + list_2[i]
    list_3.append(a)
print(list_3)


